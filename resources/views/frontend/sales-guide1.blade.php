        const backToTopBtn = document.getElementById('backToTop');

        window.addEventListener('scroll', function() {
            if(window.scrollY > 300) {
                backToTopBtn.classList.remove('hidden');
            } else {
                backToTopBtn.classList.add('hidden');
            }
        });

        backToTopBtn.addEventListener('click', function() {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });

        // Highlight current section in TOC
        const sections = document.querySelectorAll('.scroll-mt-24');
        const navLinks = document.querySelectorAll('aside nav a');

        window.addEventListener('scroll', function() {
            let current = '';

            sections.forEach(section => {
                const sectionTop = section.offsetTop;
                const sectionHeight = section.clientHeight;

                if(scrollY >= (sectionTop - 150)) {
                    current = section.getAttribute('id');
                }
            });

            navLinks.forEach(link => {
                link.classList.remove('text-emerald-600', 'font-medium', 'border-emerald-500');
                link.classList.add('text-slate-600');

                if(link.getAttribute('href') === `#${current}`) {
                    link.classList.add('text-emerald-600', 'font-medium', 'border-emerald-500');
                    link.classList.remove('text-slate-600');
                }
            });
        });

        // Add animation on scroll
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -100px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if(entry.isIntersecting) {
                    entry.target.classList.add('animate-fade-in');
                }
            });
        }, observerOptions);

        document.querySelectorAll('.article-content > div').forEach(el => {
            observer.observe(el);
        });

